public class RectanguloTema3 {
    public static void main(String arg[]) {
        int alto = 15;
        int ancho = 25;
        int area = ancho * alto;
        int perimetro = 2 * alto + 2 * ancho;
        System.out.println("El área del rectangulo de alto " + alto + " cm y de ancho " + ancho + " cm es: " + area + "cm");
        System.out.println("El perímetro de rectangulo de de alto " + alto + " cm y de ancho " + ancho + " cm es: " + perimetro + "cm\u00b2");
    }
}