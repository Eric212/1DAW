package como.ericsospedra.Tema10.Ejercicio10;
public class Son {
    private int age;
    private String name;
    public Son(int edad, String nombre) {
        this.age = edad;
        this.name = nombre;
    }
    public int getEdad() {
        return age;
    }
    public void setEdad(int edad) {
        this.age = edad;
    }
    public String getNombre() {
        return name;
    }
    public void setNombre(String nombre) {
        this.name = nombre;
    }
}
